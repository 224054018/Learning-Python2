# Dictionary: is a collection of key value pairs unlike lists or tuples which are index by a range 
# of numbers dictionaries are index by keys which can be any type like string or numeric data type

my_dict = {'apple' : 3, 'banana': 5, 'orange': 2}
print(my_dict['apple'])

my_dict['grapes'] = 4
print(my_dict)

my_dict['banana'] = 7
print(my_dict)